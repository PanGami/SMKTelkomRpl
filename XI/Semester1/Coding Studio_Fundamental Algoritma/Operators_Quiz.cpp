#include<stdio.h>

int main(){
	int i,j;
	
	scanf("%d %d", &i, &j);
	printf("%d\n", i+j);
	//Tambah
	
	scanf("%d %d", &i, &j);
	printf("%d\n", i-j);
	//kurang
	
	scanf("%d %d", &i, &j);
	printf("%d\n", i*j);
	//Kali
	
	scanf("%d %d", &i, &j);
	printf("%d\n", i/j);
	//Bagi
		
	scanf("%d %d", &i, &j);
	printf("%d\n", i%j);
	//Modulo
	
	return 0;
}

package Metic;
public class OperatorLogika {
    /*  Operator Logika
            &&      AND
            ||      OR
            !       NOT     dari yang true menjadi false dan sebaliknya ex !(a==a)
    */    
        public static void main(String[] args) {
            
            if(1==1 || 2==1){
                System.out.println("True");
            }else{
                System.out.println("False");
            }
    }
}

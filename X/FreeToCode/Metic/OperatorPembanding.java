package Metic;
public class OperatorPembanding {
    /*  Membandingkan 2 nilai operand dengan hasil boolean(true atau false)
        Operator Pembanding
            ==      Sama
            !=      Tidak sama
            >       Lebih dari
            <       Kurang dari
            >=      Lebih dari sama dengan
            <=      Kurang dari sama dengan
    */
    public static void main(String[] args) {
        System.out.println(1 >= 1);
    }
}

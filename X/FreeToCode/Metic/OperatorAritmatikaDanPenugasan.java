package Metic;
public class OperatorAritmatikaDanPenugasan {
    /* Operator Aritmatika
          +     Penjumlahan
          -     Pengurangan
          *     Perkalian
          /     Pembagian
          %     Modulus(sisa hasil bagi)
    contoh : 5 + 7 (5 dan 7 sebagai operand dan + adalah operator)         
       
    Penugasan adalah memberi nilai ke variable seperti
          a = 5
          b += 5 
          c -= 5 
          d *= 5
          e /= 5
          f %= 5    
          
    */    
        public static void main(String[] args) {
            int b = 1;
            b = b+5;
            b +=5;
    }
        
}
